package Uno_Game;

public class Cards {
    public String colors;
    public int numbersOfCards;
    public int eachColor;
    public String types;

    public Cards(String colors, int numbersOfCards, int eachColor, String types) {
        this.colors = colors;
        this.numbersOfCards = numbersOfCards;
        this.eachColor = eachColor;
        this.types = types;
    }
}
